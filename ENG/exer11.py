num_x = float(input("First number: \n"))
num_y = float(input("Second number: \n"))
num_z = float(input("Third number: \n"))
multi = num_x * num_y * num_z
sum = num_x + num_y + num_z
subtraction = num_x - num_y - num_z
total_sum = sum + multi + subtraction
print(f"multication: {multi}")
print(f"sum: {sum}")
print(f"subtraction: {subtraction}")
print(f"Sum of the operations: {total_sum}")