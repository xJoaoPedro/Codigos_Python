first = float(input("first number: "))
second = float(input("second number: "))
third = float(input("third number: "))
forth = float(input("forth number: "))
fifth = float(input("fifth number: "))
mean = (first + second + third + forth + fifth) / 5
print(f"The mean of the numbers {first}, {second}, {third}, {forth} and {fifth} is: {mean}")