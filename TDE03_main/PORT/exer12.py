primeiro = float(input("Primeiro número: "))
segundo = float(input("Segundo número: "))
terceiro = float(input("Terceiro número: "))
quarto = float(input("Quarto número: "))
quinto = float(input("Quinto número: "))
media = (primeiro + segundo + terceiro + quarto + quinto) / 5
print(f"A média dos números {primeiro}, {segundo}, {terceiro}, {quarto} e {quinto} é: {media}")